CREATE DATABASE Quiz_Online
go
use Quiz_Online
go

create table Role(
roleId			varchar(10)		primary key,
roleName		varchar(50)		not null,
)
-- Create table
create table Users(
email			varchar(50)		primary key,
roleId			varchar(10)		foreign key references Role(roleId),
name			nvarchar(50)	not null,
password		nvarchar(MAX)	null,
status			int				not null
)
--Create table Subject
create table Subject(
subjectId     varchar(50)   primary key,
subjectName   nvarchar(100) not null,
timeToTakeQuiz int not null check(timeToTakeQuiz > 1),
totalOfQuestionsPerQuiz int not null check (totalOfQuestionsPerQuiz > 9),
pointPerQuestion float not null,
)


--Create table Question
create table Question(
questionId         varchar(50)   primary key,
subjectId          varchar(50)   foreign key references Subject(subjectId),
question_content   nvarchar(max) not null,
createDate         datetime      not null,
status             int           not null
)

--Create table Answer
create table Answer (
answerId    varchar(50)   primary key,
answerContent  nvarchar(MAX)   not null,
questionId     varchar(50)     foreign key references Question(questionId),
type          bit not null,
)


--Create table Quiz
create table Quiz(
quizId        varchar(50)   primary key,
userId        varchar(50)   foreign key references Users(email),
subjectId     varchar(50)   foreign key references Subject(subjectId),
quizTime      int			not null,
questionTotal int           null default 0,
maxPoint      float         not null default 0,
pointUser     float         null default 0,
createDate    datetime      not null,
status        int           not null
)

--Create table AnswerUser
create table AnswerUser(
answerUserId    varchar(50) primary key,
questionId  varchar(50) foreign key references Question(questionId),
quizId      varchar(50) foreign key references Quiz(quizId),
answerId    varchar(50) foreign key references Answer(answerId),
answer_user nvarchar(max)    null,
createDate datetime       not null,
isCorrect  bit not null
)	


